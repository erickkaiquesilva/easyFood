import UIKit

extension UIColor {
    class var backgroundWithe: UIColor {
        return UIColor(red: 255, green: 255, blue: 255, alpha: 1)
    }
    
    class var backgroundBlack: UIColor {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 1)
    }
}
